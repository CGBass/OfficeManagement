    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         try
         {
           DriverManager.registerDriver(new com.mysql.jdbc.Driver());
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/Office_db?user=root&password=");
           
           String loginid=jTextField1.getText();
           String pwd=jPasswordField1.getText();
           boolean flag=false;
           String role="";
           
           Statement st=con.createStatement();
           ResultSet rs=st.executeQuery("select * from login_tb where Login_ID='"+loginid+"' and Password='"+pwd+"'");
           while(rs.next())
           {
               flag=true;
               role=rs.getString(3);
            }
           con.close();
           
           if(flag)
           {
               JOptionPane.showMessageDialog(this,"Login Successfully..");
               this.setVisible(false);
               if(role.equals("admin"))
               {
                    Office_System ob=new Office_System();
                    ob.setVisible(true);
               }
               else
               {
                   EMP_DASHBOARD ob=new EMP_DASHBOARD();
                   ob.setVisible(true);
               }                 
           }
           else
               JOptionPane.showMessageDialog(this,"Invalid LoginId or Password..");
           
          }
          catch(Exception e)
          {
                    JOptionPane.showMessageDialog(this,"Error: "+e);
          }
    }//GEN-LAST:event_jButton1ActionPerformed

  
